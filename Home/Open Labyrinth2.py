from collections import namedtuple
from heapq import heappop, heappush, heapify


Node = namedtuple("Node", ('priority', 'cost', 'position', 'previous', 'direction'))


def heuristic(position, goal):
    return abs(position[0] - goal[0]) + abs(position[1] - goal[1])


def get_neighbour(position, maze):
    directions = {'E': (0, 1), 'W': (0, -1), 'N': (-1, 0), 'S': (1, 0)}
    neighbours = []

    for direction, (dx, dy) in directions.items():
        x = position[0] + dx
        y = position[1] + dy
        if not maze[x][y]:
            neighbours.append(((x, y), direction))

    return neighbours


def checkio(maze_map):
    #replace this for solution
    #This is just example for first maze
    start = (1, 1)
    goal = (10, 10)
    open_list = [Node(0, 0, start, None, '')]
    heapify(open_list)
    closed_list = set()

    while open_list:
        current = heappop(open_list)
        if current.position == goal:
            break
        if current.position in closed_list:
            continue
        neighbours = get_neighbour(current.position, maze_map)
        for new_position, direction in neighbours:
            if new_position in closed_list:
                continue
            priority = current.cost + 1 + heuristic(new_position, goal)
            heappush(open_list, Node(priority, current.cost + 1, new_position, current, direction))
        closed_list.add(current.position)

    result = ""
    while current.previous is not None:
        result = current.direction + result
        current = current.previous
    return result


